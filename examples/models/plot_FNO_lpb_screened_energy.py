# Why this is a natural FNO example
# ---------------------------------
# This example highlights two reasons neural operators are attractive in
# biomolecular electrostatics:
#
# 1. The labels come from a PDE solve rather than an arbitrary descriptor model.
# 2. Resolution invariance is genuinely useful because the same physical charge
#    configuration can be evaluated on multiple voxel grids in continuum-electrostatics
#    workflows.
#
# In a production setting, one would replace this synthetic homogeneous,
# periodic linearized-PB generator with outputs from a finite-difference PB
# solver or APBS pipeline for proteins, ligands, or protein-ligand complexes,
# while keeping the same ``FNO + ResolutionInvariantReadout`` structure.
